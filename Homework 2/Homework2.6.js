/*
Заданы две переменные для двух целых чисел,
найти максимальное общее значение, которое нацело делит два заданных числа.
*/

let a = 31;
let b = 10;

for (let i = a; i >= a; i++) {
	if (i % a === 0) { 
		console.log(i);
	}
}

for (let i = b; i >= b; i++) {
	if (i % b === 0) { 
		console.log(i);
	}
}